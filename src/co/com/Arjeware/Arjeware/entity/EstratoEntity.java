public class EstratoEntity {
    private int idEstrato;
    private int idCiudad;
    private String nombreEstrato;

    public int getIdEstrato() {
        return idEstrato;
    }

    public void setIdEstrato(int idEstrato) {
        this.idEstrato = idEstrato;
    }

    public int getIdCiudad() {
        return idCiudad;
    }

    public void setIdCiudad(int idCiudad) {
        this.idCiudad = idCiudad;
    }

    public String getNombreEstrato() {
        return nombreEstrato;
    }

    public void setNombreEstrato(String nombreEstrato) {
        this.nombreEstrato = nombreEstrato;
    }
    
    
}
